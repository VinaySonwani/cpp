
// Reserving memory which is known as Declaration
	// Using AMA for the fundamental Data Type
		int var;		/* In Automatic Memory Allocation, C++ itself allocates the memory equal to size of the Data Type in the program's stack at run time and 				           assigns it an address. C++ then uses a variable to name that memory address. Here, memory equal to the size of int is allocated in the 				           program's stack, an address is assigned to it and that address is given the name as var. */

// Assignment
	var = 5;

// AMA + Initialization
	int var2 = 7;

// Access
	cout << var << endl;
	cout << var2 << endl;



